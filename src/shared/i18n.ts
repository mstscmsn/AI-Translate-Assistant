export function getTranslationStyleOptions(
  t: Translator,
  settings?: Pick<Settings, "customStyleName">
): Array<{
  value: TranslationStyle;
  label: string;
  description: string;
}> {
  const presetOptions = TRANSLATION_STYLE_OPTIONS.map((style) => ({
    value: style.value,
    label: t(`style.${style.value}.label` as TranslationKey),
    description: t(`style.${style.value}.description` as TranslationKey)
  }));

  return [
    ...presetOptions,
    {
      value: "custom",
      label: settings?.customStyleName.trim() || t("style.custom.label"),
      description: t("style.custom.description")
    }
  ];
}

export function getTranslationStyleLabel(
  style: TranslationStyle,
  t: Translator,
  settings?: Pick<Settings, "customStyleName">
): string {
  return (
    getTranslationStyleOptions(t, settings).find((item) => item.value === style)
      ?.label ?? t("style.natural.label")
  );
}

export function getShortcutText(
  commandId: string,
  field: "label" | "description",
  t: Translator
): string {
  const key = `shortcut.${commandId}.${field}` as TranslationKey;
  return t(key);
}

function getSystemLanguageCode(): string {
  try {
    return chrome.i18n?.getUILanguage?.() || navigator.language || "en-US";
  } catch {
    return navigator.language || "en-US";
  }
}

function resolveSystemUILanguage(language: string): ResolvedUILanguage {
  const normalized = language.toLowerCase().replace("_", "-");
  if (normalized.startsWith("zh-tw") || normalized.startsWith("zh-hk")) return "zh-TW";
  if (normalized.startsWith("zh")) return "zh-CN";
  if (normalized.startsWith("ja")) return "ja-JP";
  if (normalized.startsWith("ko")) return "ko-KR";
  if (normalized.startsWith("fr")) return "fr-FR";
  if (normalized.startsWith("de")) return "de-DE";
  if (normalized.startsWith("es")) return "es-ES";
  if (normalized.startsWith("it")) return "it-IT";
  if (normalized.startsWith("pt")) return "pt-BR";
  if (normalized.startsWith("ru")) return "ru-RU";
  return "en-US";
}

function interpolate(
  message: string,
  values: Record<string, string | number> | undefined
): string {
  if (!values) return message;
  return Object.entries(values).reduce(
    (next, [key, value]) => next.split(`{${key}}`).join(String(value)),
    message
  );
}
