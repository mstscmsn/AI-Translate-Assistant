const dictionaries: Record<ResolvedUILanguage, Messages> = {
  "zh-CN": zhCN,
  "zh-TW": zhTW,
  "en-US": enUS,
  "ja-JP": jaJP,
  "ko-KR": koKR,
  "fr-FR": frFR,
  "de-DE": deDE,
  "es-ES": esES,
  "it-IT": itIT,
  "pt-BR": ptBR,
  "ru-RU": ruRU
};

export type TranslationKey = keyof Messages;

export type Translator = (
  key: TranslationKey,
  values?: Record<string, string | number>
) => string;

export function createTranslator(preference: UILanguage = "system"): Translator {
  const language = resolveUILanguage(preference);
  const messages = dictionaries[language];
  return (key, values) => interpolate(messages[key] ?? zhCN[key] ?? key, values);
}

export function resolveUILanguage(preference: UILanguage = "system"): ResolvedUILanguage {
  if (preference !== "system") {
    return preference;
  }
  const language = getSystemLanguageCode();
  return resolveSystemUILanguage(language);
}

